public class ForLoopBackwardsFrom100 { // Start of forLoopBackwardsFrom100 class

    public static void main(String[] args) { // Start of main method

        for(int i = 100; i >= 0; i = i - 5) { // Start of for loop, counts 20 times
            System.out.print(i + " "); // Outputs i and a space
        } // End of for loop

    } // End of main method
} // End of forLoopBackwardsFrom100 class
